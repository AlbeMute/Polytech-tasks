
#1. Дана строка, содержащая русскоязычный текст. Найти количество слов, начинающихся с буквы "е".
s = "Понятие «содержание высказывания» связано с категорией информативности речи и присуще только тексту, ель, Ехать "
count = sum(1 for word in s.split() if word[0] in ('е', 'Е'))
print("Количество слов, начинающихся с буквы 'е':", count)


#2. В строке заменить все двоеточия (:) знаком процента (%). Подсчитать количество замен.
