#5. Дан одномерный массив, состоящий из N целочисленных элементов. Ввести массив с клавиатуры. Найти минимальный элемент. Вывести индекс минимального элемента на экран.
n = int(input("Введите длину массива: "))
s = []
for i in range(n):
    e = int(input(f"Введите {i+1}-й элемент массива: "))
    s.append(e)
min_e = min(s)
min_index = s.index(min_e)
print("Индекс минимального значения: ", min_index)
    

#6. Дан одномерный массив из 8 элементов. Заменить все элементы массива меньшие 15 их удвоенными значениями. Вывести на экран монитора преобразованный массив.
from random import randint
l = [randint(1, 30) for x in range(8)]
print("Исходный массив: ", l)
l = [x * 2 if x < 15 else x for x in l]
print("Преобразованный массив:", l)






